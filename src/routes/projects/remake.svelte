<h1 class="text-center text-2xl">The Remake</h1>
<section class="py-2">
	<p>December 2021</p>
	<p>
		Nowhere can anyone find better examples of how far they've come than by looking back at their
		own projects. Some of my oldies used <code>global scripts</code>
		within the <code>index.html</code>. Web dev has come a long way. The idea of mobile first and
		responsive image sizing would've been huge development efforts not too long ago. Now I'm simply
		in shock by how quickly I can do those things with much less effort.
	</p>
	<p>
		Remaking old projects was not a straight forward decision. Doing so expands the scope of the
		project which in turn takes more time. It's usually easier to start from stratch and when I
		looked into the <code>Reveal.js</code> project all I could see was some clever css styling for
		2013. Good in it's own time but not as good as <code>tailwindcss</code> today. For the
		interested reader, I suggest watching tailwind's
		<a class="link link-primary" href="https://www.youtube.com/channel/UCOe-8z68tgw9ioqVvYM4ddQ"
			>youtube videos</a
		>. The kicker for me was that implementing the same features as <code>Reveal.js</code> is simpler
		with tailwind.
	</p>
	<p>
		Svelte's also very deserving of the lime light. Snappy navigation within the website is the
		default. Anyone on the internet these days has moved on from a website because it took too long
		to load or do something. Svelte.kit is the result of years of high requirements as well as a
		persmise of solving common issues that plague the well known frameworks. It was a no brainer for
		me.
	</p>
	<p>
		All in all, the remake only took me a couple of weeks with easy revisions as well as
		help/user-testing with friends and family. It's been great!
	</p>
